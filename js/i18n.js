const I18N = {
  get lang() { return localStorage.getItem("mh_lang") || "ar"; },
  set lang(v) { localStorage.setItem("mh_lang", v); },

  t(key, vars) {
    let str = (TRANSLATIONS[this.lang] && TRANSLATIONS[this.lang][key]) || TRANSLATIONS.en[key] || key;
    if (vars) Object.keys(vars).forEach(k => { str = str.replace(`{${k}}`, vars[k]); });
    return str;
  },

  apply() {
    const lang = this.lang;
    document.documentElement.lang = lang;
    document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
    document.querySelectorAll("[data-i18n]").forEach(el => {
      el.textContent = this.t(el.getAttribute("data-i18n"));
    });
    document.querySelectorAll("[data-i18n-ph]").forEach(el => {
      el.placeholder = this.t(el.getAttribute("data-i18n-ph"));
    });
  },

  toggle() {
    this.lang = this.lang === "en" ? "ar" : "en";
    location.reload();
  }
};

window.t = (k, v) => I18N.t(k, v);
window.I18N = I18N;
